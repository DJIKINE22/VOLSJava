     private void table_vols(){
     String []Vols={
         "Vol",
         "compagnie",
         "source",
         "destination",
         "Nobre Place",
         "date et heure"
     };
     String []  Afficher = new String[7];
     DefaultTableModel model = new DefaultTableModel(null,Vols);
     String sql= "select * from vol";
      
     try{
     Connect();
     Statement st = con.createStatement();
     rs =st.executeQuery(sql);
     
     while(rs.next()){
         Afficher[0] = rs.getString("codeVol");
         Afficher[1] = rs.getString("Source");
         Afficher[2] = rs.getString("compagnie");
         Afficher[3] = rs.getString("Destination");
         Afficher[4] = rs.getString("NbrePlace");
         Afficher[5] = rs.getString("date");
         model.addRow(Afficher);
     }
     jTable1.setModel(model);
     
     con.close();
     }catch(Exception e){
        e.printStackTrace();}
     }
